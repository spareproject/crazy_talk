#!/bin/env bash
###############################################################################################################################################################################################################
mkdir -p ./rootfs/boot
mkdir -p ./mount/
###############################################################################################################################################################################################################
mount ${1} ./rootfs/boot
pacstrap -cGMd ./rootfs $(for i in $(cat packages);do if [[ ! $(grep "#" <<< ${i}) ]];then echo -n "${i} " ;fi;done)
umount ./rootfs/boot
cp -arfv airootfs/* rootfs/
arch-chroot ./rootfs /root/install.sh ${1}
###############################################################################################################################################################################################################
umount ./rootfs/boot
rm -r ./rootfs/boot
mount ${1} ./mount
mksquashfs ./rootfs ./mount/rootfs.squashfs
echo " quick and dirty... "
###############################################################################################################################################################################################################
